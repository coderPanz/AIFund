import { Hono } from 'hono'
import { streamSSE } from 'hono/streaming'

const app = new Hono()

// AI 对话接口（流式响应）
app.post('/chat', async (c) => {
  const body = await c.req.json()
  const message = body.message || ''

  return streamSSE(c, async (stream) => {
    // 模拟 AI 响应
    const response = generateAIResponse(message)

    // 逐字符发送，模拟流式响应
    for (const char of response) {
      await stream.writeSSE({ data: char })
      await new Promise(resolve => setTimeout(resolve, 20))
    }
  })
})

// 生成 AI 响应
function generateAIResponse(message: string): string {
  const lowerMsg = message.toLowerCase()

  // 关键词匹配
  if (lowerMsg.includes('科技') || lowerMsg.includes('科技')) {
    return `关于科技主题基金，我有以下建议：

1. **指数型基金**：如纳斯达克100指数基金（广发纳斯达克100），可以一键布局全球科技巨头。

2. **主动管理型**：关注投资科技股的主动基金，如易方达信息产业、华宝科技先锋等。

3. **细分赛道**：半导体、人工智能、新能源等细分领域也有专门的ETF可供选择。

**投资建议**：
- 科技板块波动较大，建议分批建仓或定投
- 关注基金的持仓集中度，避免过度集中于单一股票
- 长期持有，忽略短期波动

**风险提示**：科技股估值较高，波动性大，请根据自身风险承受能力谨慎投资。`
  }

  if (lowerMsg.includes('债券') || lowerMsg.includes('低风险')) {
    return `关于低风险债券基金，我推荐以下几类：

1. **纯债基金**：如博时信用债券A，主要投资债券，波动小，收益稳定。

2. **一级债基**：可参与新股申购，收益略高于纯债。

3. **短债基金**：投资短期债券，流动性好，风险更低。

**选择建议**：
- 关注基金规模，建议选择10亿以上的产品
- 查看历史最大回撤，选择回撤小的产品
- 对比费率，选择费率较低的产品

**收益预期**：债券基金年化收益一般在3%-6%之间，适合稳健型投资者。`
  }

  if (lowerMsg.includes('定投') || lowerMsg.includes('指数')) {
    return `关于指数基金定投，我建议：

**推荐指数**：
1. **宽基指数**：沪深300、中证500、创业板指
2. **行业指数**：消费、医药、科技等长期看好的行业
3. **海外指数**：纳斯达克100、标普500

**定投策略**：
- 每月固定金额投资
- 设置止盈点（如30%），达到后赎回继续定投
- 市场大跌时可增加定投金额

**费率优势**：指数基金管理费低，长期投资成本低，适合定投。

**时间周期**：建议定投周期3年以上，穿越牛熊周期。`
  }

  if (lowerMsg.includes('配置') || lowerMsg.includes('组合')) {
    return `关于资产配置，我建议根据风险偏好分层配置：

**保守型（风险等级1-2）**：
- 债券基金：60-80%
- 股票/混合基金：20-40%

**平衡型（风险等级3）**：
- 债券基金：40-50%
- 股票/混合基金：40-50%
- 海外基金：10%

**进取型（风险等级4-5）**：
- 股票/混合基金：60-80%
- 海外基金：10-20%
- 债券基金：10-20%

**配置原则**：
- 不要把鸡蛋放在一个篮子里
- 定期再平衡，保持配置比例
- 根据人生阶段调整配置

您可以前往"资产配置"页面完成风险测评，获取个性化配置建议。`
  }

  // 默认回复
  return `感谢您的咨询！我可以帮您解答以下问题：

1. **基金推荐**：根据您的投资目标推荐合适的基金
2. **基金分析**：分析基金的投资价值和风险收益特征
3. **投资建议**：提供资产配置和定投策略建议

请告诉我您的投资需求，比如：
- "我想投资科技主题基金"
- "低风险的债券基金有哪些"
- "想定投指数基金，选哪个好"
- "如何构建资产组合"

我会尽力为您提供专业的建议。`
}

export default app